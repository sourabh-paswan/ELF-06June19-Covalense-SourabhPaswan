package com.covalense.java.basics;

public class Vendingmachine {

	public static void main(String[] args) {
		int a= 20;
		switch(a) {
		case 1: System.out.println("give chips");
		break;

		case 20: System.out.println("give kurkure");
		break;

		case 3: System.out.println("give dairy milk");
		break;

		default: System.out.println("invalid data");
		}

	}
}
