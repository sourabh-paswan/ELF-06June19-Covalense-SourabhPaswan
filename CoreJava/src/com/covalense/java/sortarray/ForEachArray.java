package com.covalense.java.sortarray;

public class ForEachArray {
	public static void main(String[] args) {
		String[] s= {"aas","tr","rte","hh","ded","ty"};
		int[] i= {23,43,54,87};
		double[] d= {43.5,453.6,343.6,45.6};
		
	for(int r: i)
	{
		System.out.println(r);
	}
	for(String r: s)
	{
		System.out.println(r);
	}
	for(double r: d)
	{
		System.out.println(r);
	}
	}

}
